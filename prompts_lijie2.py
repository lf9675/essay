# 5. 对比标准答案  （四色比对法，逐采分点）
# ============================================================
def prompt_compare(article, question, model_answer, scoring_points, student_answer):
    sp = "；".join(scoring_points) if isinstance(scoring_points, list) else str(scoring_points)
    return (
        "你是新加坡高级华文 O 水准资深评卷员，用「四色比对法」逐采分点批改学生答案。"
        "评分必须严格遵守六条铁律，尤其是「关键词精准」「不能照抄」「解释要结合上下文」。\n\n"
        + SCORING_RULES +
        "\n请逐个采分点判断：✓ 全对、△ 方向对但漏关键词、✗ 完全没答到。"
        "对每个 △ 和 ✗，要指出「该得分点对应原文的哪一句 / 哪个关键词」，让学生知道下次去原文哪里取词。\n\n"
        "只输出 JSON，不要任何前后文字、不要 markdown：\n"
        "{\n"
        '  "total_marks": "本题满分数字",\n'
        '  "got_marks": "学生实得分数字",\n'
        '  "point_check": [\n'
        '    {"point": "采分点内容", "status": "✓/△/✗", "comment": "为什么", "from_text": "对应原文出处 / 关键词，✓可留空"}\n'
        "  ],\n"
        '  "rule_flag": "若学生触犯了某条铁律（如照抄原文 / 概括词不准 / 没结合上下文 / 没表态），在此点明，无则空",\n'
        '  "one_fix": "最该改进的一点（离满分最近的那个△）",\n'
        '  "encouragement": "一句鼓励，符合中低程度学生，真诚不浮夸"\n'
        "}\n\n"
        "【篇章】\n【" + article + "】\n\n"
        "【题目】" + question + "\n"
        "【采分点】" + sp + "\n"
        "【标准答案】" + model_answer + "\n"
        "【学生答案】" + student_answer
    )


