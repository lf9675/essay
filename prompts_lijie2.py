# 4. 引导答题  （针对单题，给思路 + 模板，但先不给答案）
# ============================================================
def prompt_guide(article, question, qtype):
    templates = {
        "复述题": "（谁）+（在什么情况下）+（做了什么）+（结果怎样）。先概括出一个精准的词，再回原文锁定证据。",
        "分析题": "因为（前文具体事件 / 处境）→ 所以（人物的反应）→ 这反映了（性格 / 心情 / 主旨）。原因往前找，意义往后想。",
        "推断题": "从文中「……（引关键词）」可以看出，（推断结论），因为（简短理由）。先找原文暗示，再补一句逻辑。",
        "技能题-描写作用": "运用了（动作 / 语言 / 神态 / 心理）描写，生动形象地写出了……的（特点 / 心情），表现了（性格 / 主旨）。",
        "技能题-修辞作用": "运用了（比喻 / 拟人 / 排比……），把……生动形象地写出了……的特点，表达了……的感情。",
        "技能题-解释句意": "这句话表面是说……，结合上下文，实际指的是……。比喻句必须把喻体还原成文中实指。",
        "开放题": "我同意（明确表态）。作者认为……（回扣文中关键词）。在生活中，例如……（具体的本地化情境）。这件事说明……（拉回作者观点）。",
    }
    tpl = ""
    for k, v in templates.items():
        if k in qtype:
            tpl = v
            break
    return (
        "你是新加坡高级华文特级教师，正在一对一辅导一名中低程度学生。"
        "学生还没作答，请先「引导」他思考——不要直接给答案，而是给思路、给模板、提醒陷阱。\n\n"
        + SCORING_RULES +
        "\n本题题型：" + qtype + "\n"
        "参考答题模板：" + tpl + "\n\n"
        "请输出 JSON，不要任何前后文字、不要 markdown：\n"
        "{\n"
        '  "marks_hint": "这题几分，应拆成几个点（提醒学生先数分）",\n'
        '  "thinking_steps": ["引导思考的步骤1", "步骤2", "步骤3"],\n'
        '  "where_to_look": "答案大概在文章的什么位置去找",\n'
        '  "template": "适合本题的填空式答题模板",\n'
        '  "trap": "本题最容易踩的失分陷阱"\n'
        "}\n\n"
        "【篇章】\n【" + article + "】\n\n【题目】" + question
    )


