# ============================================================
# 0. 共同的「评分铁律」—— 注入到所有 prompt，确保 AI 与官方评分一致
# ============================================================
SCORING_RULES = """
【新加坡 O 水准高级华文理解二·六条评分铁律】（必须严格遵守）
1. 关键词必须精准：概括词错了直接 0 分（如把「专注」答成「坚强」、把「点拨」答成「帮助」），找对原文也救不回。
2. 不能照抄原文：「指什么 / 为什么」类题目，整段照抄最多给一半分；必须删并换，转成自己的话。
3. 解释句意必须「结合上下文」：这是得分动作不是口号；比喻句要把喻体还原成文中实指；不结合上下文，解释对了也只给 1 分甚至 0 分。
4. 按分值拆点作答：4 分题往往是「两组、每组词+证据」；5 分题往往是「表态 + 主旨 + 分述」。先数分，再列点。
5. 5 分大题先定调：是否矛盾 / 是否同意类，不先明确表态全题 0 分。
6. 开放题不自由：必须表态、必须用上文章核心关键词、例子必须具体且回扣作者观点；漏关键词直接封顶 3 分。
"""

# ============================================================
# 1. 拆解段落 + 标注中心  （第一步，合并一次调用省 token）
# ============================================================
def prompt_segment_and_center(article):
    return (
        "你是新加坡资深高级华文特级教师，专教中低程度中学生应对 O 水准理解二。"
        "请帮学生「拆解段落」并「标注中心」。\n\n"
        "【拆段方法】每个自然段给一个不超过 8 字的标签，格式「谁+做什么」或「什么+怎么样」。"
        "若全文可分大层次（按时间 / 地点 / 事件 / 情感变化），指出分层处。\n"
        "【标中心】找出全文中心句（先扫段首、段尾、末段），并指出每段是否含中心句。"
        "标出所有转折词（但是 / 然而 / 没想到 / 可是 / 却）所在段落——转折后几乎一定有考点。\n\n"
        "只输出 JSON，不要任何前后说明文字、不要 markdown 代码块。结构如下：\n"
        "{\n"
        '  "paragraphs": [\n'
        '    {"num": 1, "label": "8字内标签", "center_sentence": "本段中心句原文，无则空字符串", "has_turn": true/false, "turn_word": "转折词，无则空"}\n'
        "  ],\n"
        '  "layers": "全文大层次划分说明，一句话",\n'
        '  "main_idea": "全文中心 / 主旨，一句话",\n'
        '  "reading_tip": "给中低程度学生的一句拆段提醒"\n'
        "}\n\n"
        "【篇章】\n【" + article + "】"
    )


# ============================================================
# 2. 预测考点  （四问扫描法）
# ============================================================
def prompt_predict(article):
    return (
        "你是新加坡高级华文特级教师。请用「四问扫描法」预测这篇文章在 O 水准理解二最可能出的考点，"
        "帮学生学会看穿命题人的心思。\n\n"
        "四问：① 哪里有「反常」（人物做不合常理的事）→ 多出分析题；"
        "② 哪里有「漂亮句子」（比喻 / 拟人 / 排比 / 细致描写）→ 多出技能题；"
        "③ 哪里有「难懂的句子」（含蓄 / 双关 / 言外之意）→ 多出解释句意题；"
        "④ 哪里是「情感最高点」→ 多出主旨题或开放题。\n\n"
        "请预测 4 到 6 个考点。只输出 JSON，不要任何前后文字、不要 markdown：\n"
        "{\n"
        '  "predictions": [\n'
        '    {"location": "出现在第几段 / 哪句", "type": "复述题/分析题/推断题/技能题-描写作用/技能题-修辞作用/技能题-解释句意/开放题",\n'
        '     "why": "为什么这里会被出题，一句话", "likely_question": "命题人很可能这样问：……"}\n'
        "  ]\n"
        "}\n\n"
        "【篇章】\n【" + article + "】"
    )


# ============================================================
# 3. AI 自动出题  （参考 O 水准真题题型，出 5 题 16-20）
# ============================================================
def prompt_generate_questions(article):
    return (
        "你是新加坡高级华文 O 水准命题专家。请仿照 O 水准理解二（A 组，共 38 分）的真实题型，"
        "为以下篇章命制 5 道题（编号 16-20），题型与分值分布须贴近真题：\n"
        "- 第 16 题：态度 / 心情 / 看法概括题（含「从哪里看出」或「为什么」），约 4 分\n"
        "- 第 17 题：解释 + 推因题（「指什么」+「什么促使 / 为什么」），约 4 分\n"
        "- 第 18 题：结合上下文解释两句话的意思（含一个比喻 / 含蓄句），约 4 分（2+2）\n"
        "- 第 19 题：5 分大分析题（心态转变 / 是否矛盾 / 前后关系）\n"
        "- 第 20 题：5 分开放题（是否同意作者某观点，结合现实生活例子）\n\n"
        + SCORING_RULES +
        "\n命题后，请同时给出官方风格的「采分点参考答案」，把每道题拆成可计分的得分点。\n"
        "只输出 JSON，不要任何前后文字、不要 markdown：\n"
        "{\n"
        '  "questions": [\n'
        '    {"num": 16, "type": "题型", "marks": 4, "question": "题干",\n'
        '     "scoring_points": ["得分点1（1分）", "得分点2（1分）"],\n'
        '     "model_answer": "整合后的参考答案"}\n'
        "  ]\n"
        "}\n\n"
        "【篇章】\n【" + article + "】"
    )


